// This program demonstrates a simple function that takes two integers as input and returns their sum.
int add(int a, int b) {
  return a + b;
}

// This program demonstrates a simple function that takes two integers as input and returns their product.
int multiply(int a, int b) {
  return a * b;
}

int main() {
  // Prints the sum of 2 and 3.
  printf("%d\n", add(2, 3));

  // Prints the product of 4 and 5.
  printf("%d\n", multiply(4, 5));

  return 0;
}
